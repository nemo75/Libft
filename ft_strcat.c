#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	lg_s1;
	size_t	i;

	i = 0;
	lg_s1 = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[lg_s1++] = s2[i++];
	}
	s1[lg_s1] = '\0';
	return (s1);
}
