#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new_list;

	new_list = (t_list *)malloc(sizeof(t_list));
	if (new_list == NULL)
		return (NULL);
	if (lst == NULL)
		return (NULL);
	new_list = (*f)(lst);
	new_list->next = ft_lstmap(lst->next, f);
	return (new_list);
}
